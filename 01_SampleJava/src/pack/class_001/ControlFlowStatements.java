package pack.class_001;

public class ControlFlowStatements {

	public static void main(String[] args) {
		
		// only 'if'
		if(true) {
			System.out.println("called");
		}
		
		// 'if' and 'else'
		if(3>=2) {
			System.out.println("3 is greater or equal then 2");
		} else {
			System.out.println("3 is less then 2");
		}

	}

}
